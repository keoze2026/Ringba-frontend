/* ===========================================================
   Payout history — used on the publisher detail "Payouts" tab.
   =========================================================== */

function periodLabel(daysAgoStart: number, daysAgoEnd: number) {
  const fmt = (d: Date) => `${d.getFullYear()}-${(d.getMonth() + 1).toString().padStart(2, "0")}-${d.getDate().toString().padStart(2, "0")}`;
  const s = new Date(Date.now() - daysAgoStart * DAY);
  const e = new Date(Date.now() - daysAgoEnd * DAY);
  return `${fmt(s)} → ${fmt(e)}`;
}

