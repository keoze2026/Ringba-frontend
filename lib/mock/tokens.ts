/**
 * Mock cryptocurrency dataset for the Coinmarket page.
 *
 * Modeled after the CoinMarketCap "Top" table: rank, name/symbol, price,
 * 1h/24h/7d % change, market cap, 24h volume, circulating supply, and a
 * compact 7-day sparkline series.
 *
 * Designed to plug into a real source (CoinGecko / CoinMarketCap API) later
 * with no consumer changes — same `TokenEntry` shape.
 */

export interface TokenEntry {
  rank: number;
  symbol: string;
  name: string;
  /** Two-color gradient that stands in for the token logo when no `logoUrl`. */
  tint: [string, string];
  /** Real-source logo URL (CoinGecko / CMC). When present the table renders this
   *  instead of the gradient fallback. */
  logoUrl?: string;
  /** Last price in USD. */
  price: number;
  change1h: number;
  change24h: number;
  change7d: number;
  marketCap: number;
  volume24h: number;
  /** Tokens in circulating supply (in the token's own unit). */
  circulatingSupply: number;
  /** 25 evenly-spaced price points covering the trailing week. */
  sparkline: number[];
}

/* ─── Sparkline helper ───────────────────────────────────────────────── */

/** Produce a believable 7-day price walk based on a final % move. */
function spark(price: number, change7d: number, seed: number): number[] {
  const start = price / (1 + change7d / 100);
  const steps = 25;
  const out: number[] = [];
  let v = start;
  // Pseudo-random walk that lands close to `price` at the end.
  const r = (i: number) => {
    // Mulberry32 — deterministic per (seed, i).
    let t = (seed + i * 374761393) | 0;
    t = ((t ^ (t >>> 15)) * 2246822519) | 0;
    t = ((t ^ (t >>> 13)) * 3266489917) | 0;
    return ((t ^ (t >>> 16)) >>> 0) / 4294967296;
  };
  for (let i = 0; i < steps; i++) {
    const trend = (price - start) / (steps - 1);
    const noise = (r(i) - 0.5) * Math.abs(price) * 0.025;
    v = i === steps - 1 ? price : v + trend + noise;
    out.push(Number(v.toFixed(price < 1 ? 5 : 2)));
  }
  return out;
}

