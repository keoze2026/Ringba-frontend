/* ─── Display helpers ────────────────────────────────────────────────── */

const NF = new Intl.NumberFormat("en-US");
const CF_USD = new Intl.NumberFormat("en-US", {
  style: "currency",
  currency: "USD",
  minimumFractionDigits: 2,
  maximumFractionDigits: 2,
});
const CF_USD_PRECISE = new Intl.NumberFormat("en-US", {
  style: "currency",
  currency: "USD",
  minimumFractionDigits: 4,
  maximumFractionDigits: 7,
});

/** "$98,412.55" — uses extra decimals for tiny prices. */
export function formatTokenPrice(p: number): string {
  if (p < 1) return CF_USD_PRECISE.format(p);
  return CF_USD.format(p);
}

/** "$1.95T" / "$142.31B" / "$98.4M" / "$1.2K" — short compact form. */
export function formatCompactCurrency(n: number): string {
  if (Math.abs(n) >= 1_000_000_000_000) return `$${(n / 1_000_000_000_000).toFixed(2)}T`;
  if (Math.abs(n) >= 1_000_000_000) return `$${(n / 1_000_000_000).toFixed(2)}B`;
  if (Math.abs(n) >= 1_000_000) return `$${(n / 1_000_000).toFixed(2)}M`;
  if (Math.abs(n) >= 1_000) return `$${(n / 1_000).toFixed(2)}K`;
  return `$${n.toFixed(2)}`;
}

/** "20.03M BTC" / "589.28T SHIB" — supply with token unit. */
export function formatSupply(n: number, symbol: string): string {
  if (n >= 1_000_000_000_000) return `${(n / 1_000_000_000_000).toFixed(2)}T ${symbol}`;
  if (n >= 1_000_000_000) return `${(n / 1_000_000_000).toFixed(2)}B ${symbol}`;
  if (n >= 1_000_000) return `${(n / 1_000_000).toFixed(2)}M ${symbol}`;
  if (n >= 1_000) return `${(n / 1_000).toFixed(2)}K ${symbol}`;
  return `${NF.format(n)} ${symbol}`;
}
